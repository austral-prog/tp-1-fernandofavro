def math():
    a = 57
    b = 7

    # 1. Suma
    print(a + b)

    # 2. Diferencia
    print(a - b)

    # 3. Producto
    print(a * b)

    # 4. Promedio
    print((a + b) / 2)

    # 5. Cociente entero
    print(a // b)

    # 6. Resto de la división
    print(a % b)

    # 7. Valor real de la división
    print(a / b)
